import java.util.Scanner;


public class ClientMain {
	

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		String input;
		System.out.print("> ");
		Scanner sc = new Scanner(System.in);
		input = sc.nextLine();
		while (!input.equals("exit")){
			//command loop
			System.out.print("> ");
			
			
			
			//next command
			input = sc.nextLine();
		}

	}

}
